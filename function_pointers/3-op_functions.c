#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - function
 * @a: first integer 
 * @b: second integer
 *
 */
int op_add(int a, int b)
{
  return (a + b);
}

/**
 * op_sub - function
 * @a: first integer
 * @b: second integer
 *
 */
int op_sub(int a, int b)
{
  return (a - b);
}

/**
 * op_mul - function
 * @a: first integer
 * @b: second integer
 *
 */
int op_mul(int a, int b)
{
  return (a * b);
}

/**
 * op_div - function
 * @a: first integer
 * @b: second integer
 *
 */
int op_div(int a, int b)
{
  return (a / b);
}

/**
 * op_mod - function
 * @a: first integer
 * @b: second integer
 *
 */
int op_mod(int a, int b)
{
  return (a % b);
}
